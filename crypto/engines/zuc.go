// NewZUCEngine creates a new ZUC-128 stream cipher engine.
func NewZUCEngine() *ZUCEngine {
	return &ZUCEngine{
		s0:        s0,
		s1:        s1,
		lfsr:      make([]uint32, 16),
		keyStream: make([]uint32, 2),
	}
}

// Init initializes the cipher.
//
// Parameters:
//   - forEncryption: ignored (stream ciphers are symmetric)
//   - params: must be ParametersWithIV containing a KeyParameter
func (z *ZUCEngine) Init(forEncryption bool, p crypto.CipherParameters) error {
	// Extract key and IV from parameters
	paramsWithIV, ok := p.(*params.ParametersWithIV)
	if !ok {
		return errors.New("ZUC init parameters must include an IV (use ParametersWithIV)")
	}

	iv := paramsWithIV.GetIV()
	keyParam, ok := paramsWithIV.GetParameters().(*params.KeyParameter)
	if !ok {
		return errors.New("ZUC init parameters must include a KeyParameter")
	}

	key := keyParam.GetKey()

	if len(key) != 16 {
		return errors.New("ZUC requires a 128-bit key")
	}

	if len(iv) != 16 {
		return errors.New("ZUC requires a 128-bit IV")
	}

	z.workingKey = make([]byte, 16)
	z.workingIV = make([]byte, 16)
	copy(z.workingKey, key)
	copy(z.workingIV, iv)

	z.setKeyAndIV(z.workingKey, z.workingIV)
	z.initialized = true

	return nil
}

// GetAlgorithmName returns the algorithm name.
func (z *ZUCEngine) GetAlgorithmName() string {
	return "ZUC-128"
}

// ReturnByte encrypts/decrypts a single byte.
func (z *ZUCEngine) ReturnByte(input byte) (byte, error) {
	if !z.initialized {
		return 0, errors.New("ZUC not initialized")
	}

	if z.keyStreamIndex == 0 {
		z.generateKeyStream()
	}

	out := input ^ z.getKeyStreamByte()
	return out, nil
}

// ProcessBytes processes a block of bytes.
func (z *ZUCEngine) ProcessBytes(input []byte, inOff int, length int, output []byte, outOff int) (int, error) {
	if !z.initialized {
		return 0, errors.New("ZUC not initialized")
	}

	if inOff+length > len(input) {
		return 0, errors.New("input buffer too short")
	}

	if outOff+length > len(output) {
		return 0, errors.New("output buffer too short")
	}

	for i := 0; i < length; i++ {
		if z.keyStreamIndex == 0 {
			z.generateKeyStream()
		}

		output[outOff+i] = input[inOff+i] ^ z.getKeyStreamByte()
	}

	return length, nil
}

// Reset resets the cipher.
func (z *ZUCEngine) Reset() {
	if z.workingKey != nil && z.workingIV != nil {
		z.setKeyAndIV(z.workingKey, z.workingIV)
	}
	z.initialized = z.workingKey != nil
}

// setKeyAndIV sets key and IV, initializes LFSR and discards first 32 words.
func (z *ZUCEngine) setKeyAndIV(key []byte, iv []byte) {
	// Loading sequence defined in ZUC specification
	d := []byte{
		0x44, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
		0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x3f,
	}

	for i := 0; i < 16; i++ {
		z.lfsr[i] = z.makeU31(
			(uint32(key[i]) << 23) | (uint32(d[i]) << 8) | uint32(iv[i]),
		)
	}

	z.r1 = 0
	z.r2 = 0
	z.keyStreamIndex = 0

	// Run 32 iterations in initialization mode
	for i := 0; i < 32; i++ {
		w := z.f()
		z.lfsrWithInitMode(w >> 1)
	}

	// Generate first keystream word (discard)
	z.f()
	z.lfsrWithWorkMode()
}

// makeU31 makes a 31-bit value from a 32-bit input by masking the MSB.
func (z *ZUCEngine) makeU31(value uint32) uint32 {
	return value & 0x7fffffff
}

// lfsrWithInitMode updates LFSR in initialization mode.
func (z *ZUCEngine) lfsrWithInitMode(u uint32) {
	s16 := z.lfsr[15]
	s0 := z.lfsr[0]

	v := z.makeU31(
		(s0 << 8) ^ z.mulByPow2(s0, 20) ^
			(s16 << 21) ^ z.mulByPow2(s16, 17) ^
			z.mulByPow2(s16, 15),
	)

	s16mod := z.makeU31(s16 + u)

	// Shift LFSR
	for i := 0; i < 15; i++ {
		z.lfsr[i] = z.lfsr[i+1]
	}

	z.lfsr[15] = z.makeU31(v + s16mod)
}

// lfsrWithWorkMode updates LFSR in working mode.
func (z *ZUCEngine) lfsrWithWorkMode() {
	s16 := z.lfsr[15]
	s0 := z.lfsr[0]

	v := z.makeU31(
		(s0 << 8) ^ z.mulByPow2(s0, 20) ^
			(s16 << 21) ^ z.mulByPow2(s16, 17) ^
			z.mulByPow2(s16, 15),
	)

	// Shift LFSR
	for i := 0; i < 15; i++ {
		z.lfsr[i] = z.lfsr[i+1]
	}

	z.lfsr[15] = z.makeU31(v + s16)
}

// mulByPow2 performs multiplication by 2^k in GF(2^31-1).
func (z *ZUCEngine) mulByPow2(x uint32, k uint) uint32 {
	return ((x << k) | (x >> (31 - k))) & 0x7fffffff
}

// bitReorganization performs bit reorganization.
func (z *ZUCEngine) bitReorganization() [4]uint32 {
	x0 := ((z.lfsr[15] & 0x7fff8000) << 1) | (z.lfsr[14] & 0xffff)
	x1 := ((z.lfsr[11] & 0xffff) << 16) | (z.lfsr[9] >> 15)
	x2 := ((z.lfsr[7] & 0xffff) << 16) | (z.lfsr[5] >> 15)
	x3 := ((z.lfsr[2] & 0xffff) << 16) | (z.lfsr[0] >> 15)

	return [4]uint32{x0, x1, x2, x3}
}

// sBox performs S-box lookup (combining S0 and S1).
func (z *ZUCEngine) sBox(x uint32) uint32 {
	return (uint32(z.s0[(x>>24)&0xff]) << 24) |
		(uint32(z.s1[(x>>16)&0xff]) << 16) |
		(uint32(z.s0[(x>>8)&0xff]) << 8) |
		uint32(z.s1[x&0xff])
}

// l1 performs linear transformation L1.
func (z *ZUCEngine) l1(x uint32) uint32 {
	return x ^ z.rot(x, 2) ^ z.rot(x, 10) ^ z.rot(x, 18) ^ z.rot(x, 24)
}

// l2 performs linear transformation L2.
func (z *ZUCEngine) l2(x uint32) uint32 {
	return x ^ z.rot(x, 8) ^ z.rot(x, 14) ^ z.rot(x, 22) ^ z.rot(x, 30)
}

// rot performs 32-bit rotation.
func (z *ZUCEngine) rot(x uint32, n uint) uint32 {
	return (x << n) | (x >> (32 - n))
}

// f performs the nonlinear function F.
func (z *ZUCEngine) f() uint32 {
	br := z.bitReorganization()
	x0, x1, x2 := br[0], br[1], br[2]

	w := x0 ^ z.r1 ^ z.r2
	w1 := z.r1 + x1
	w2 := z.r2 ^ x2

	u := z.l1((w1 << 16) | (w2 >> 16))
	v := z.l2((w2 << 16) | (w1 >> 16))

	z.r1 = z.sBox(u)
	z.r2 = z.sBox(v)

	return w
}

// generateKeyStream generates key stream words.
func (z *ZUCEngine) generateKeyStream() {
	x3_1 := z.bitReorganization()[3]
	z.keyStream[0] = z.f() ^ x3_1
	z.lfsrWithWorkMode()
	x3_2 := z.bitReorganization()[3]
	z.keyStream[1] = z.f() ^ x3_2
	z.lfsrWithWorkMode()
	z.keyStreamIndex = 0
}

// getKeyStreamByte gets one byte from the key stream buffer.
func (z *ZUCEngine) getKeyStreamByte() byte {
	word := z.keyStream[z.keyStreamIndex>>2]
	byteIndex := 3 - (z.keyStreamIndex & 3)
	z.keyStreamIndex = (z.keyStreamIndex + 1) & 7
	return byte((word >> (byteIndex * 8)) & 0xff)
}
