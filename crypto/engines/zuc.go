// ZUCEngine implements the ZUC-128 stream cipher algorithm.
//
// ZUC is a stream cipher algorithm designed for use in 3GPP confidentiality
// and integrity algorithms 128-EEA3 and 128-EIA3.
//
// Standards: GM/T 0001-2012, 3GPP TS 35.221
// Reference: org.bouncycastle.crypto.engines.ZucEngine
type ZUCEngine struct {
	// S-boxes
	s0 []byte
	s1 []byte

	// LFSR - 16 cells of 31 bits each
	lfsr []uint32

	// Registers R1 and R2
	r1 uint32
	r2 uint32

	// Key stream buffer
	keyStream      []uint32
	keyStreamIndex int

	// Initialization parameters
	initialized bool
	workingKey  []byte
	workingIV   []byte
}

